pub async fn filter(c: char) -> char {
    match c {
        'A'..='Z' => c.to_ascii_lowercase(),
        'a'..='z' => c,
        '0'..='9' => c,
        
        '!' => '1',
        '@' => '2',
        '#' => '3',
        '$' => '4',
        '%' => '5',
        '^' => '6',
        '&' => '7',
        '*' => '8',
        '(' => '9',
        ')' => '0',
        
        '_' => '-',
        '-' => c,

        '+' => '=',
        '=' => c,

        '{' => '[',
        '[' => c,

        '}' => ']',
        ']' => c,

        '\\' => '|',
        '|' => c,

        ':' => ';',
        ';' => c,

        '"' => '\'',
        '\'' => c,

        '<' => ',',
        ',' => c,

        '>' => '.',
        '.' => c,

        '?' => '/',
        '/' => c,
        
        _ => ' ',
    }
}